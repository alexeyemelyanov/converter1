package ru.ewromet.converter2.parser;

public class Tool {

    private String name;
    private String length;
    private String time;
    private String cutoutArea;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLength() {
        return length;
    }

    public void setLength(String length) {
        this.length = length;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getCutoutArea() {
        return cutoutArea;
    }

    public void setCutoutArea(String cutoutArea) {
        this.cutoutArea = cutoutArea;
    }
}
